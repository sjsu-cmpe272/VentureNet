function CheckWidgetSearchFields()
{
    var animalType = document.getElementById("sortbar-animaltype");
    var breedType = document.getElementById("sortbar-breedtype");
    var zip = document.getElementById("input-zip");

    if (animalType.value == "0")
    {
        alert("Please select an animal type");
        return false;
    }

    if (breedType.value == "")
    {
        alert("Please select a breed type");
        return false;
    }

    if (zip.value == "" || !zip.value.match(/(^\d{5}(-\d{4})?$)|(^[ABCEGHJKLMNPRSTVXY]{1}\d{1}[A-Z]{1} *\d{1}[A-Z]{1}\d{1}$)/i))
    {
        alert("Please enter zip/postal code");
        return false;
    }

    return true;
}

function dosearchWidget() {

    if (CheckWidgetSearchFields()) {
        var searchLink = GetWidgetSearchLink();
        __doPostBack('', searchLink);
    }
}

function GetWidgetSearchLink()
{
    var animalType = document.getElementById("sortbar-animaltype");
    var breedType = document.getElementById("sortbar-breedtype");
    var zip = document.getElementById("input-zip");

    var searchLink = "../Forms/AnimalSearchResultsAd.aspx?s=" + animalType.value + "&b=" + breedType.value + "&z=" + zip.value + "&d=35";
    return searchLink;
}

